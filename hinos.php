                                   <h4 >O Hino Nacional</h4>
                                   <p> O <a href="https://pt.wikipedia.org/wiki/Hino_Nacional_Brasileiro"> Hino Nacional </a> tem letra de <a href="https://pt.wikipedia.org/wiki/Os%C3%B3rio_Duque-Estrada"> Joaquim Osório Duque Estrada e música de Francisco Manuel da Silva (1795 - 1865)</a> e foi adquirido por cinco contos de réis à propriedade plena e definitiva da letra do hino pelo decreto n.º 4 559 de 21 de agosto de 1922 pelo então presidente <a href="https://pt.wikipedia.org/wiki/Hino_Nacional_Brasileiro"> Epitácio Pessoa </a> e oficializado pela lei n.º 5 700, de 1971. É executado em continência à Bandeira Nacional e ao <a href="https://pt.wikipedia.org/wiki/Presidente_do_Brasil"> presidente da República, ao <a href="https://pt.wikipedia.org/wiki/Congresso_Nacional_do_Brasil"> Congresso Nacional</a> e ao <a href="https://pt.wikipedia.org/wiki/Supremo_Tribunal_Federal"> Supremo Tribunal Federal,</a> assim como em outros casos determinados pelos regulamentos de continência ou cortesia internacional. Sua execução também é permitida na abertura de sessões cívicas, cerimônias religiosas de caráter patriótico e antes de eventos esportivos internacionais.<a href="https://pt.wikipedia.org/wiki/S%C3%ADmbolos_do_Brasil#cite_note-L5700-2">[2]</a></p>

