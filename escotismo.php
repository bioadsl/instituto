            <legend>Saiba o que é escotismo</legend>
            <p>O escotismo é um movimento que surgiu na Inglaterra no ano de 1907 e foi criado por Baden-Powell, tenente
                do exército britânico.
                Este movimento surgiu com o objetivo de aperfeiçoar os conhecimentos dos jovens e desenvolver princípios
                morais, cívicos e organizacionais.<br> 
                A sua proposta é o desenvolvimento do jovem, por meio de um sistema de valores que prioriza a honra, baseado na Promessa (ou Compromisso) e na Lei escoteira (ou Lei do Escoteiro), e através da prática do trabalho em equipe e da vida ao ar livre, fazendo com que o jovem assuma seu próprio crescimento, tornando-se um exemplo de fraternidade, lealdade, companheirismo, altruísmo, responsabilidade, respeito e disciplina.<br>
                Entusiasmado com os bons resultados deste acampamento, Baden-Powell começou a escrever o livro Escotismo para Rapazes, que foi publicado em 1908, inicialmente como seis fascículos, de janeiro a maio, vendido em bancas de jornal. Em maio do mesmo ano, foi editado como livro com ligeiras modificações.<br>
                O livro fez muito sucesso entre professores de educação física, e depois da insistência de vários líderes da juventude, Baden-Powell decidiu adaptar seu manual de escotismo especificamente para treinamento de meninos. A recepção das ideias de Baden-Powell foi tanta que, em poucas semanas, centenas de Patrulhas Escoteiras estavam formadas, praticando Escotismo. Rapidamente o movimento se espalhou por vários países do mundo, chegando à América do Sul em 1908, ao Chile.<br><br>

<b><i>"O Escotismo é uma escola de cidadania através da destreza e habilidade em assuntos mateiros."
—Robert Baden-Powell, Escotismo para Rapazes, 1908</i></b>
                
                </p>
                
                
            
