                <legend> O objetivo do jogo é queimar o maior número possível de jogadores adversários.</legend>
                    <div style="text-align: justify;">
            Para esta brincadeira você vai precisar de:
            1 bola de tamanho médio – a bola de vôlei é a ideal
            Giz ou uma varinha para marcar a quadra (dependendo do tipo de chão do local escolhido)
            Um terreno amplo e plano: quadra de esportes, pátio ou praça
            Tempo: Não é fixo, dependendo da vontade dos jogadores ou sendo combinado previamente, antes do jogo
            Marca-se a quadra com três linhas de mais ou menos 10 metros de distância uma da outra
            </div>

            Como brincar
            <div style="text-align: justify;">1) Os jogares são divididos em dois grupos de números iguais, separados pela linha do meio.</div>
            <div style="text-align: justify;">2) Sorteia-se qual grupo vai jogar primeiro, e por sua vez este escolhe um de seus jogadores para começar.</div>
            <div style="text-align: justify;">3) O jogador escolhido inicia o jogo ficando em pé perto da linha central e arremessando a bola com força para queimar (isso é, atingir) um dos adversários; estes podem fugir da bola, desde que não saiam dos limites da quadra.</div>
            <div style="text-align: justify;">4) O objetivo é queimar o maior número possível de jogadores adversários, até não restar nenhum. Se um jogador pegar a bola no ar, ele corre até a linha central e arremessa a bola para o outro time para queimar um adversário.</div>
            <div style="text-align: justify;">5) Mas se a bola cair no chão, o jogador mais próximo deve pegá-la e arremessá-la do ponto onde ela caiu.</div>
            <div style="text-align: justify;">6) É queimado o jogador que for acertado e deixar a bola cair.</div>
            <div style="text-align: justify;">7) Se ele conseguir pegar a bola depois de atingido, ou se algum colega a pegar, o jogador não é queimado.</div>
            <div style="text-align: justify;">8) O jogador queimado tem que ir para trás da linha que marca o limite final da quadra adversária – chamada de “prisão”, ou “cemitério”, e jogar a bola de lá para seu próprio time.</div>
            <div style="text-align: justify;">9) Ele ainda pode jogar, mas somente se a bola cair dentro do cemitério, pois não podem sair desta área.</div>
            <div style="text-align: justify;">10) De lá, ele pode acertar os adversários ou jogar a bola de volta para os colegas.</div>
            <div style="text-align: justify;">11) Se o jogador queimado conseguir queimar um adversário, ele pode voltar a jogar dentro da sua área.</div>
            <div style="text-align: justify;">12) Vence o time que conseguir queimar todos os jogadores ou queimar a maior quantidade possível dentro do prazo previamente combinado.</div>



