                            Resumo da história do xadrez 
                            <p>O jogo se originou na Índia do século VI, sendo chamado de Shaturanga, que 
                            significa “os quatro componentes de um exército” em Sanscrito. Assim, nele 
                            estavam representados a infantaria, cavalaria e o povo (peões, cavalos e 
                            rainha), bem como as carroças e os elefantes. Posteriormente, com as rotas 
                            comerciais, o jogo foi levado à China e Pérsia, onde recebeu o nome de 
                            xadrez, decorrente da palavra persa shah (rei). 
                            Da Pérsia, o xadrez migrou para o norte africano, Espanha e Portugal, 
                            difundindo-se pela Europa no período entre os séculos VIII e XV. Nesse 
                            percurso na Espanha e Ex-União Soviética que incorporou elementos 
                            tipicamente medievais, como as figuras das torres dos castelos e dos bispos, 
                            que substituíram as carroças e os elefantes, respectivamente. Além disso, 
                            desse processo decorre a formulação das regras que configuram e organizam 
                            o jogo em seu formato atual.</p> 
                            
                            <p>A lenda do xadrez</p> 
                            
                            <p>A lenda do xadrez diz respeito a um rei indiano que foi presenteado com o 
                            jogo por um brâmane. Assim, como forma de agradecimento, o rei exige que 
                            o brâmane peça uma retribuição. Frente à exigência, portanto, ele pede que 
                            o rei lhe conceda grãos de trigo exponencialmente multiplicados para cada 
                            uma das 64 casas do tabuleiro. 
                            Diante disso, o rei teria que dar ao brâmane 18.446.744.073.709.551.615 
                            grãos de trigo. Entretanto, o rei percebeu que não seria capaz de cumprir a 
                            promessa de lhe dar essa quantia, e o brâmane perdoou sua dívida. Com isso, 
                            o rei foi presenteado com o jogo e também com a prudência de não prometer 
                            coisas que não pudesse cumprir.</p>
                            
                            
                            <p>Regras básicas do Xadrez.  </p>
                            
                            <li>O tabuleiro de xadrez é composto por 64 quadrados (casas), sendo 
                            cada um desses organizados em cores alternadas (preto e branco) e 
                            identificados por sua posição em colunas alfabéticas e fileiras 
                            numéricas (de A a H e de 1 a 8). </li>
                            <li> O jogo é iniciado pelo jogador com as peças brancas e o tabuleiro deve 
                            ser posicionado de modo que cada jogador tenha uma casa preta em 
                            seu canto inferior esquerdo. </li>
                            <li> As peças são compostas por 8 peões, 2 torres, 2 bispos, 2 cavalos 1 rei 
                            e 1 rainha, sendo que cada uma dessas possui recursos de 
                            movimentação específicos. </li>
                            <li> O objetivo do jogo é aplicar um xeque-mate no rei do jogador 
                            oponente e, ao mesmo tempo, não permitir que ele aplique um xequemate no seu rei. </li>
                            <li> O xeque-mate diz respeito a uma situação em que a peça do rei não 
                            pode ser movida para nenhuma posição do tabuleiro e/ou não está 
                            protegida por nenhuma de suas peças.</li> 
                           
